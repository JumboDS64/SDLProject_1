GLuint tex_font1;
glm::vec3 titleTextPos = glm::vec3(11.5, 12, 0);

bool GameState::addEntity(Entity* e) {
    if (entities_cur < entities_max) {
        entities[entities_cur] = e;
        e->id = entities_cur;
        entities_cur += 1;
        return true;
    }
    else {
        return false;
    }
}
bool GameState::destroyEntity(int id) {
    if (entities_cur > 0) {
        delete entities[id];
        entities[id] = entities[entities_cur];
        entities[id]->id = id;
        entities[entities_cur] = NULL;
        entities_cur -= 1;
        return true;
    }
    else {
        return false;
    }
}

